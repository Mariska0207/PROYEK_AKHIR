from autentikasi import login, registrasi
import inquirer
import os

while True:
    os.system("cls || clear")
    menu = [
        inquirer.List("opsi",
                    message="TOKO FASHION ADALACE ₍^. .^₎Ⳋ",
                    choices=["✎.. Registrasi", "⌯⌲. Login", "✈︎. Keluar"],
                ),
    ]
    answer = inquirer.prompt(menu)
    menu = answer["opsi"]
    os.system("cls || clear")

    if "✎" in menu:
        registrasi()
        input("Enter untuk kembali ke menu....")

    elif "⌯⌲" in menu:
        login()
        input("Enter untuk kembali ke menu....")

